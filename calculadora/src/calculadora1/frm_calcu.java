    private void txt_resultadoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_resultadoKeyPressed

        switch (evt.getKeyCode()) {
            case KeyEvent.VK_PLUS -> {
                op.setN1(Float.parseFloat(this.txt_resultado.getText()));
                opcion =1;
            }
            case KeyEvent.VK_MINUS -> {
                op.setN1(Float.parseFloat(this.txt_resultado.getText()));
                opcion =2;
            }
            case KeyEvent.VK_MULTIPLY -> {
                op.setN1(Float.parseFloat(this.txt_resultado.getText()));
                opcion =3;
            }
            case KeyEvent.VK_DIVIDE -> {
                op.setN1(Float.parseFloat(this.txt_resultado.getText()));
                opcion =4;
            }
            case KeyEvent.VK_ENTER -> {
            }
            default -> {
            }
        }

        switch(opcion){
            case 1 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.suma();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" + "+op.getN2()+" = "+op.getResultado());
            }
            case 2 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.resta();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" - "+op.getN2()+" = "+op.getResultado());
            }
            case 3 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.multi();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" * "+op.getN2()+" = "+op.getResultado());
            }
            case 4 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.divi();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" / "+op.getN2()+" = "+op.getResultado());
            }
        }
    }//GEN-LAST:event_txt_resultadoKeyPressed

