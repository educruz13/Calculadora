    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed

        
        switch(opcion){
            case 1 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.suma();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" + "+op.getN2()+" = "+op.getResultado());
                }
            case 2 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.resta();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" - "+op.getN2()+" = "+op.getResultado());
                }
            case 3 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.multi();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" * "+op.getN2()+" = "+op.getResultado());
                }
            case 4 -> {
                op.setN2(Float.parseFloat(this.txt_resultado.getText()));
                op.divi();
                this.txt_resultado.setText(op.resultado+"");
                Archivo.escribir("Historial.txt","" +op.getN1()+" / "+op.getN2()+" = "+op.getResultado());
                }
        }
    }//GEN-LAST:event_btn_igualActionPerformed

